      <h2><b>PRIERE A L'ESPRIT SAINT</b></h2>
      <hr>
      <hr>
      <hr>
      <p>Viens, Esprit-Saint !<br>  Viens habiter en nos coeurs.<br>Envoie du haut du ciel un rayon de ta lumière.</p>
      <p>Toi, le père des pauvres, viens !<br>Toi qui distribut les dons de Dieu !<br>Toi, la lumière des coeurs, viens !</p>
      <p>Toi, le consolateur tout-puissant, viens !<br>Toi, l'hôte trés doux de nos âmes, viens !<br>Toi, la douce fraîcheur, viens !</p>
      <p>A qui est fatigué, apporte le repos.<br>A qui est fiévreux, apporte la fraîcheur.<br>A qui pleure, apporte la consolation.</p>
      <p>ô lumière qui apporte la vrai joie,<br>viens remplir le coeur de tes disciples.<br>Sans ta puissance divine,<br>il n'est rien de bon en l'homme.</p>
      <p>Celui qui s'est sali, lave-le.<br>Celui qui a soif, donne-lui à boire.<br>Celui qui est blessé, soigne-le.<br>
      Celui qui est raide, rend-le souple.<br>Celui qui est froid, réchauffe-le.<br>Celui qui est courbé, redresse-le.</p>
      <p>A ceux qui croient et qui ont confiance en toi,<br>Donne tous tes dons sacrés.<br>Donne ce qui est bien et bon.<br>Donne-nous le salut au dernier jour.<br>Donne-nous la joie éternelle.<b>Amen</b></p>
